/*Given x=3.0, y=12.5, z= 523.3, A=300.0, B=1200.5, C=5300.3, Write a program to display the following:
x y z= 3.0| 12.5| 523.3|
A B C= 300.0| 1200.5| 5300.3|
---------------------------------------------------------------------
x y z= |3.00 |12.50 |523.30
A B C= |300.00 |1200.50 |52300.30
*/
#include<stdio.h>
#include<conio.h>
int main()
{
    float x,y,z,A,B,C;
    x=3.0,y=12.5,z=523.3;
    A=300.0,B=1200.5,C=5300.3;
    printf("%c%3c%4s%7.1f%c%8.1f%c%8.1f%c\n",'x','y',"z=",x,'|',y,'|',z,'|');
    printf("%c%3c%4s%7.1f%c%8.1f%c%8.1f%c",'A','B',"C=",A,'|',B,'|',C,'|');
    printf("\n----------------------------------\n");
    printf("%c%3c%4s%3c%-7.2f%c%-8.2f%c%-8.2f\n",'x','y',"z=",'|',x,'|',y,'|',z);
    printf("%c%3c%4s%3c%-7.2f%c%-8.2f%c%-8.2f\n",'A','B',"C=",'|',A,'|',B,'|',C);
}


